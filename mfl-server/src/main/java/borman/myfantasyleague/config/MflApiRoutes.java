package borman.myfantasyleague.config;

public class MflApiRoutes {

    private String leagueInfo;
    private String rosterInfo;
    private String playerInfo;
    private String tradeBlock;

    public String getLeagueInfo() {
        return leagueInfo;
    }

    public void setLeagueInfo(String leagueInfo) {
        this.leagueInfo = leagueInfo;
    }

    public String getRosterInfo() {
        return rosterInfo;
    }

    public void setRosterInfo(String rosterInfo) {
        this.rosterInfo = rosterInfo;
    }

    public String getPlayerInfo() {
        return playerInfo;
    }

    public void setPlayerInfo(String playerInfo) {
        this.playerInfo = playerInfo;
    }

    public String getTradeBlock() {
        return tradeBlock;
    }

    public void setTradeBlock(String tradeBlock) {
        this.tradeBlock = tradeBlock;
    }
}